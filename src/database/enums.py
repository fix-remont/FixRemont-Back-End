from enum import Enum


class ProjectType(str, Enum):
    FLAT = "Квартира"
    HOUSE = "Дом"

    def __str__(self):
        return self.value


class PostType(str, Enum):
    NEWS = "Новость"
    BLOG = "Блог"

    def __str__(self):
        return self.value


class UserType(str, Enum):
    REALTOR = "Риэлтор"
    DEVELOPER = "Заказчик"
    INDIVIDUAL = "Индивидуальный предприниматель"

    def __str__(self):
        return self.value


